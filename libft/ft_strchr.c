#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char		*mark;

	mark = 0;
	while (*s)
	{
		if (*s == c)
			return ((char*)s);
		s++;
	}
	if (c == '\0')
		return ((char*)s);
	return (0);
}
